// 프리미엄 라이선스 검증 (강화된 시뮬레이션 — HMAC 체크섬 방식)
// ------------------------------------------------------------------
// 실제 서비스에서는 Gumroad/결제 시스템과 연동한 원격 라이선스 서버에
// 키 유효성(발급 여부·환불/취소 여부)을 조회하는 것이 정석입니다.
// 다만 이 npm 패키지는 오프라인 stdio MCP 서버이므로 매 호출마다 네트워크
// 요청을 강제하기보다, "형식만 맞으면 통과"하던 기존 방식을 개선해
// HMAC-SHA256 체크섬으로 자체 검증 가능한 키 구조를 사용합니다.
//
// 키 형식: KRFIN-XXXX-YYYY-ZZZZ
//   - XXXX, YYYY: 암호학적 난수(crypto.randomBytes) 기반 4자리 세그먼트
//   - ZZZZ: HMAC-SHA256(secret, `${XXXX}-${YYYY}`)에서 유도한 체크섬 4자리
// generateLicenseKey()로 생성한 키만 checksum이 일치해 통과하며,
// "KRFIN-AAAA-BBBB-CCCC" 같은 임의 문자열은 형식은 맞아도 체크섬이
// 일치하지 않아 거부됩니다.
//
// 한계(설계 문서 monetization-plan.md §8 참고): 이 secret이 npm 패키지에
// 그대로 배포되므로, 패키지를 분석하면 이론상 유효한 키를 스스로 만들어낼
// 수 있습니다(오프라인 라이선스의 공통적 한계). 프로덕션 전환 시에는
// (1) 원격 라이선스 서버 조회로 전환하거나 (2) 서버만 아는 비밀키로
// 서명하고 클라이언트는 공개키로 서명만 검증하는 비대칭 방식으로
// 업그레이드해야 합니다.
const LICENSE_SECRET =
  process.env.KR_FINANCE_LICENSE_SECRET || "kr-finance-mcp-demo-secret-2026";

// 혼동되기 쉬운 0/O, 1/I 를 제외한 Base32 스타일 알파벳
const LICENSE_ALPHABET = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";

function randomLicenseSegment(length = 4) {
  const bytes = crypto.randomBytes(length);
  let out = "";
  for (let i = 0; i < length; i++) {
    out += LICENSE_ALPHABET[bytes[i] % LICENSE_ALPHABET.length];
  }
  return out;
}

function licenseChecksumSegment(part1, part2, length = 4) {
  const digest = crypto
    .createHmac("sha256", LICENSE_SECRET)
    .update(`${part1}-${part2}`)
    .digest();
  let out = "";
  for (let i = 0; i < length; i++) {
    out += LICENSE_ALPHABET[digest[i] % LICENSE_ALPHABET.length];
  }
  return out;
}

/**
 * 신규 라이선스 키를 생성한다. (결제 완료 시 라이선스 발급 서버가 호출할
 * 함수 — 이 데모에서는 로컬에서 직접 호출해 형식/체크섬 검증을 확인한다.)
 */
function generateLicenseKey() {
  const part1 = randomLicenseSegment(4);
  const part2 = randomLicenseSegment(4);
  const part3 = licenseChecksumSegment(part1, part2);
  return `KRFIN-${part1}-${part2}-${part3}`;
}

const LICENSE_KEY_PATTERN = /^KRFIN-([A-Z0-9]{4})-([A-Z0-9]{4})-([A-Z0-9]{4})$/;

function isValidLicenseKey(key) {
  if (typeof key !== "string") return false;
  const match = LICENSE_KEY_PATTERN.exec(key.trim().toUpperCase());
  if (!match) return false;
  const [, part1, part2, part3] = match;
  return licenseChecksumSegment(part1, part2) === part3;
}

