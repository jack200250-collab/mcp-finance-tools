// 6. 환율 변환 계산기 (프리미엄)
// ------------------------------------------------------------------
// 실시간 환율 API 연동 없음 — 사용자가 제공한 환율값을 그대로 사용합니다.
function calc환율변환({ 금액, 기준통화, 대상통화, 환율 }) {
  if (typeof 금액 !== "number" || !isFinite(금액) || 금액 < 0) {
    throw new Error("금액은 0 이상의 숫자여야 합니다.");
  }
  if (typeof 환율 !== "number" || !isFinite(환율) || 환율 <= 0) {
    throw new Error(
      "환율은 0보다 큰 숫자여야 합니다. (1 기준통화 = 환율 × 1 대상통화)"
    );
  }
  if (!기준통화 || !대상통화) {
    throw new Error("기준통화와 대상통화 코드를 입력해야 합니다. (예: USD, KRW)");
  }

  const 변환금액 = round(금액 * 환율, 2);

  return {
    입력_금액: 금액,
    기준통화,
    대상통화,
    적용환율: 환율,
    변환금액,
    설명:
      `${금액.toLocaleString("ko-KR")} ${기준통화}를 1 ${기준통화} = ${환율} ${대상통화} 환율로 환산하면 ` +
      `${변환금액.toLocaleString("ko-KR")} ${대상통화}입니다. ` +
      `(실시간 환율 API 연동 없음 — 사용자가 입력한 환율값을 그대로 사용한 단순 계산입니다.)`,
  };
}

