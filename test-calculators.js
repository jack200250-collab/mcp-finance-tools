"use strict";
/**
 * test-calculators.js
 * -----------------------------------------------------------------------
 * MCP 프로토콜을 거치지 않고 calculators.js의 순수 함수 6개를 직접
 * 호출해 샘플 입력 -> 출력이 상식적인 범위인지 눈으로 확인하기 위한
 * 간단한 스모크 테스트 스크립트. (자동 assert 일부 + 콘솔 출력)
 *
 * 실행: node test-calculators.js
 * -----------------------------------------------------------------------
 */

const calc = require("./calculators.js");

let failed = 0;

function check(label, condition, detail) {
  const mark = condition ? "PASS" : "FAIL";
  if (!condition) failed++;
  console.log(`[${mark}] ${label}${detail ? " - " + detail : ""}`);
}

function section(title) {
  console.log("\n" + "=".repeat(70));
  console.log(title);
  console.log("=".repeat(70));
}

// ------------------------------------------------------------------
// 1. 4대보험 계산기 - 월급 300만원
// ------------------------------------------------------------------
section("1. calc_4대보험 (월급 3,000,000원)");
const r1 = calc.calc4대보험({ 월급: 3_000_000 });
console.log(r1);
check(
  "공제합계가 월급의 5~10% 범위 (상식적 범위)",
  r1.공제합계 > 3_000_000 * 0.05 && r1.공제합계 < 3_000_000 * 0.1,
  `공제합계=${r1.공제합계}`
);
check("세후 월급이 원래 월급보다 작음", r1.세후_월급_4대보험만공제 < 3_000_000);

// ------------------------------------------------------------------
// 2. 연봉 실수령액 계산기 - 연봉 4,000만원 (질문 예시: 3천만원대 실수령 예상)
// ------------------------------------------------------------------
section("2. calc_연봉실수령액 (연봉 40,000,000원)");
const r2 = calc.calc연봉실수령액({ 연봉: 40_000_000 });
console.log(r2);
check(
  "연 실수령액이 3,000만원대 (상식적 범위: 3,300만~3,800만)",
  r2.연_실수령액_근사치 >= 33_000_000 && r2.연_실수령액_근사치 <= 38_000_000,
  `연실수령액=${r2.연_실수령액_근사치}`
);
check("월 실수령액이 연 실수령액의 1/12과 근접", Math.abs(r2.월_실수령액_근사치 * 12 - r2.연_실수령액_근사치) < 10);

// 추가 샘플: 연봉 6,000만원
section("2-1. calc_연봉실수령액 (연봉 60,000,000원, 참고용)");
const r2b = calc.calc연봉실수령액({ 연봉: 60_000_000 });
console.log(r2b);
check(
  "연봉 6천만원 실수령액이 연봉보다 작고 4천만원대 이상",
  r2b.연_실수령액_근사치 < 60_000_000 && r2b.연_실수령액_근사치 > 45_000_000
);

// ------------------------------------------------------------------
// 3. 퇴직금 계산기 - 근속 3년, 최근 3개월 급여합계 900만원(월 300만원 x 3)
// ------------------------------------------------------------------
section("3. calc_퇴직금 (근속 3년, 최근3개월 급여합계 9,000,000원)");
const r3 = calc.calc퇴직금({ 근속연수: 3, 최근3개월_급여합계: 9_000_000 });
console.log(r3);
check(
  "3년 근속 예상 퇴직금이 대략 월급 3개월치 근처(약 900만원 안팎)",
  r3.예상_퇴직금 > 8_000_000 && r3.예상_퇴직금 < 10_000_000,
  `예상퇴직금=${r3.예상_퇴직금}`
);

// ------------------------------------------------------------------
// 4. 양도소득세 간이 계산기 - 프리미엄 게이트 테스트 (라이선스 없음)
// ------------------------------------------------------------------
section("4. calc_양도소득세_간이 (프리미엄 게이트 - 라이선스 미설정)");
delete process.env.KR_FINANCE_LICENSE_KEY;
const gateResult = calc.requirePremiumLicense();
console.log(gateResult);
check("라이선스 없으면 게이트가 막힘 (ok=false)", gateResult.ok === false);

section("4-1. calc_양도소득세_간이 (프리미엄 게이트 통과 - 발급된 유효 키 설정)");
const generatedKey = calc.generateLicenseKey();
console.log("생성된 라이선스 키:", generatedKey);
process.env.KR_FINANCE_LICENSE_KEY = generatedKey;
const gateResult2 = calc.requirePremiumLicense();
check("generateLicenseKey()로 생성한 키면 게이트 통과 (ok=true)", gateResult2.ok === true);

section("4-1b. 라이선스 체크섬 위조 방지 확인");
check(
  "형식은 맞지만 체크섬이 틀린 임의 키는 거부됨 (isValidLicenseKey=false)",
  calc.isValidLicenseKey("KRFIN-AB12-CD34-EF56") === false
);
const tampered = generatedKey.slice(0, -1) + (generatedKey.slice(-1) === "A" ? "B" : "A");
check(
  "발급된 키의 마지막 글자를 변조하면 거부됨",
  calc.isValidLicenseKey(tampered) === false,
  `원본=${generatedKey} 변조=${tampered}`
);

const r4 = calc.calc양도소득세_간이({ 양도차익: 100_000_000, 보유기간_년: 4 });
console.log(r4);
check(
  "과세표준이 양도차익보다 작음 (공제 반영됨)",
  r4.과세표준 < 100_000_000
);
check(
  "총부담세액이 양도차익보다 작음 (상식적 범위)",
  r4.총부담세액 < 100_000_000 && r4.총부담세액 > 0,
  `총부담세액=${r4.총부담세액}`
);

section("4-2. calc_양도소득세_간이 (단기보유 6개월, 단일세율 70% 확인)");
const r4b = calc.calc양도소득세_간이({ 양도차익: 50_000_000, 보유기간_년: 0.5 });
console.log(r4b);
check("단기보유 세율이 70%로 적용됨", r4b.적용세율 === 0.7);

// ------------------------------------------------------------------
// 5. DSR/DTI 계산기 - 연소득 6천만원, 신규대출 3억, 금리 4%, 30년
// ------------------------------------------------------------------
section("5. calc_DSR_DTI (연소득 60,000,000원, 신규대출 300,000,000원/4%/30년)");
const r5 = calc.calcDSR_DTI({
  연소득: 60_000_000,
  기존대출_연간원리금상환액: 0,
  기존대출_연간이자상환액: 0,
  신규대출_원금: 300_000_000,
  신규대출_연이율: 4,
  신규대출_만기년: 30,
});
console.log(r5);
check(
  "DSR이 0~100% 범위의 상식적인 값",
  r5.DSR_퍼센트 > 0 && r5.DSR_퍼센트 < 100,
  `DSR=${r5.DSR_퍼센트}%`
);
check(
  "신규대출 월상환액이 대출원금 대비 합리적 범위(약 100만원~200만원)",
  r5.신규대출_월상환액_원리금균등 > 1_000_000 && r5.신규대출_월상환액_원리금균등 < 2_000_000,
  `월상환액=${r5.신규대출_월상환액_원리금균등}`
);

// ------------------------------------------------------------------
// 6. 환율 변환 계산기 - 1000 USD를 KRW로, 환율 1,380
// ------------------------------------------------------------------
section("6. calc_환율변환 (1000 USD -> KRW, 환율 1,380)");
const r6 = calc.calc환율변환({ 금액: 1000, 기준통화: "USD", 대상통화: "KRW", 환율: 1380 });
console.log(r6);
check("1000 USD * 1380 = 1,380,000 KRW 정확히 일치", r6.변환금액 === 1_380_000);

// ------------------------------------------------------------------
// 7. 청약 가점 계산기 - 무주택 8년, 부양가족 2명, 청약통장 가입 10년
// ------------------------------------------------------------------
section("7. calc_청약가점 (무주택 8년 / 부양가족 2명 / 청약통장 10년)");
const r7 = calc.calc청약가점({
  무주택기간_년: 8,
  부양가족수: 2,
  청약통장가입기간_년: 10,
});
console.log(r7);
check("무주택기간 8년 -> 18점 (2 + 8*2)", r7.무주택기간_점수 === 18);
check("부양가족 2명 -> 15점 (5 + 2*5)", r7.부양가족_점수 === 15);
check("청약통장 가입 10년 -> 12점 (10 + 2)", r7.청약통장가입기간_점수 === 12);
check(
  "총점 = 18 + 15 + 12 = 45점, 84점 만점 이하",
  r7.총점 === 45 && r7.총점 <= 84,
  `총점=${r7.총점}`
);

section("7-1. calc_청약가점 (만점 근처: 무주택 20년/부양가족 8명/가입 20년)");
const r7b = calc.calc청약가점({
  무주택기간_년: 20,
  부양가족수: 8,
  청약통장가입기간_년: 20,
});
console.log(r7b);
check("무주택기간 상한 32점 적용", r7b.무주택기간_점수 === 32);
check("부양가족 상한 35점 적용", r7b.부양가족_점수 === 35);
check("청약통장 상한 17점 적용", r7b.청약통장가입기간_점수 === 17);
check("총점이 만점 84점을 넘지 않음", r7b.총점 === 84);

section("7-2. calc_청약가점 (프리미엄 게이트 미설정 상태 재확인)");
delete process.env.KR_FINANCE_LICENSE_KEY;
const gateResult3 = calc.requirePremiumLicense();
check("라이선스 삭제 후 다시 게이트가 막힘 (ok=false)", gateResult3.ok === false);

// ------------------------------------------------------------------
// 결과 요약
// ------------------------------------------------------------------
section("테스트 요약");
if (failed === 0) {
  console.log("모든 체크 통과.");
} else {
  console.log(`${failed}개 체크 실패.`);
  process.exitCode = 1;
}
